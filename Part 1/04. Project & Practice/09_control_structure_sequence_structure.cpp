#include <iostream>
using namespace std;

int main(){
    //Sequence Structure in C++
    
    // Entry
    //"<<"--> insertion operator
    //">>"--> extruction operator
    int a, b, c;
    cout<<"Enter the value of a."<<endl;
    cin>>a;
    cout<<"Enter the value of b"<<endl;
    cin>>b;
    // Action -1
    c = a + b;
    // Action -2
    cout<<"The sum of a and b is: "<<c<<endl;
    return 0;
    //exit
}
   