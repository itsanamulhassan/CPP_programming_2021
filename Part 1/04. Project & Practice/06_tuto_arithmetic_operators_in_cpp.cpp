# include <iostream> 

using namespace std; 

int main(){
    int a = 4, b = 5;
    cout<<"Operator in C++ Program."<<endl;
    cout<<"Following are thr types of operator in C++."<<endl;
    cout<<"The value of a + b is: "<<a+b<<endl;
    cout<<"The value of a - b is: "<<a-b<<endl;
    cout<<"The value of a * b is: "<<a*b<<endl;
    cout<<"The value of a / b is: "<<a/b<<endl;
    cout<<"The value of a % b is: "<<a%b<<endl;
    cout<<"The value of a++ is: "<<a++<<endl;
    cout<<"The value of a-- is: "<<a--<<endl;
    cout<<"The value of ++a is: "<<++a<<endl;
    cout<<"The value of --a is: "<<--a<<endl;
    cout<<endl;
return 0;
}